package geso.traphaco.erp.beans.phieuphache.imp;

public class ThongTinNhapKho {
	private String solo;
	private String vitri;
	private String sothung;
	private String some;
	private String soluongchitiet;
	
	public ThongTinNhapKho() {
		this.solo = "";
		this.vitri = "";
		this.sothung = "";
		this.some = "";
		this.soluongchitiet = "";
	}
	
	public String getSolo() {
		return solo;
	}
	public void setSolo(String solo) {
		this.solo = solo;
	}
	public String getVitri() {
		return vitri;
	}
	public void setVitri(String vitri) {
		this.vitri = vitri;
	}
	public String getSothung() {
		return sothung;
	}
	public void setSothung(String sothung) {
		this.sothung = sothung;
	}
	public String getSome() {
		return some;
	}
	public void setSome(String some) {
		this.some = some;
	}
	public String getSoluongchitiet() {
		return soluongchitiet;
	}
	public void setSoluongchitiet(String soluongchitiet) {
		this.soluongchitiet = soluongchitiet;
	}
	
}
